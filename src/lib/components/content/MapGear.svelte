	<h2>How to Install Maps &amp; Gear</h2>
	<p>
		To install custom maps and gear, simply place them in the following directories. Make sure to
		extract the files from the ZIP folder!
	</p>
	<p>Please note that XLGM gear requires you to install each piece of gear into two folders.</p>

	<h3>Custom Maps:</h3>
	<p>Documents/SkaterXL/Maps</p>

	<h3>Custom Gear:</h3>
	<p>Documents/SkaterXL/Gear</p>

	<h3>XLGM Gear (Textures):</h3>
	<p>Documents/SkaterXL/Gear</p>

	<h3>XLGM Gear (Assets):</h3>
	<p>Documents/Documents/SkaterXL/XLGearModifier/AssetPacks</p>
